/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode(int x) { val = x; }
 * }
 */
class Solution101 {
    public static class TreeNode {
        int val;
        TreeNode left;
        TreeNode right;
        TreeNode() {}
        TreeNode(int val) { this.val = val; }
        TreeNode(int val, TreeNode left, TreeNode right) {
            this.val = val;
            this.left = left;
            this.right = right;
        }
    }
    
    // 100% 82.29%
    // å¾ˆç®€å•ï¼Œæ—¢ç„¶å¯¹ç§°ï¼Œé‚£ä¹ˆå…ˆå·¦åæ ¹åå³ = å…ˆå³åæ ¹åå·¦
    public static boolean isSymmetric(TreeNode root) {
        return compare(root, root);
    }

    private static boolean compare(TreeNode root, TreeNode copy){
        if(root == null && copy == null) return true;
        if(root == null || copy == null) return false;
        if(root.val == copy.val){
            // ç»§ç»­åˆ¤æ–­, leftæ˜¯å¦ç­‰äºright
            if(compare(root.left, copy.right)){
                return compare(root.right, copy.left);
            }
        }
        return false;
    }
}
